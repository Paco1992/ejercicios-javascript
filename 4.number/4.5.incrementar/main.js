console.log(puntaje++);//Primero se llama al puntaje y da 0/
console.log(puntaje);//Aquí el valor es 1 porque en este caso se incrementa/

console.log(++puntaje)//Aquí primero hace el cálculo internamente de lo que trae y le suma el incermento después, por eso se hace en 2 pasos como arriba

console.log(puntaje+=5);//7
